// short code [wp-addpub bannerID="1" zone="zone" filter="filter" random=true]
function wp_addpub_shortcode( $atts ) {
	extract( shortcode_atts( array(
		'bannerID' => false, 'zone' => '', 'filter' => '', 'random' => 'false'
	), $atts ) );
	
	return wp_addpub ($atts, false);
}
add_shortcode( 'wp-addpub', 'wp_addpub_shortcode' );

