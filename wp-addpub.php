/*
*	Displays a banner. Use it in frontend
*	@function  wp_addpub
*	@param type string : bannerID and/or zone and/or filter (ex: "")
*   @example "zone=sidebar&filter=FR"
*	@return boolean
*/

function wp_addpub($args = "", $echo = true) {
    global $wpdb, $table_prefix;

    $table_banners  = $table_prefix . "addpub";

    load_plugin_textdomain('wp-addpub', "wp-content/plugins/wp-addpub/");

    $defaults = array(
		'bannerID' => false, 'zone' => '', 'filter' => '', 'random' => 'false'
	);

	$r = wp_parse_args( $args, $defaults );
	extract($r, EXTR_OVERWRITE );

	$WHERE = "active = 1 ";
    $ORDERBY = "";

	if ( $r['bannerid'] != '')  { 
		$bannerID = $r['bannerid'];  $WHERE .= "AND ID = '$bannerID' ";
	} elseif ( $r['bannerID'] != '')  { 
		$bannerID = $r['bannerID'];  $WHERE .= "AND ID = '$bannerID' ";
	}
    if ( $r['zone'] != '')      { $zone = $r['zone']; $WHERE .= "AND zone = '$zone' "; }
    if ( $r['filter'] != '')    { $filter = $r['filter']; $WHERE .= "AND filter = '$filter' ";}
    if ( strtolower($r['random']) === "true")    { $ORDERBY = "ORDER BY RAND()";}

    $mimetypes = array ('jpg|jpeg|jpe' => 'image/jpeg',
    		'gif' => 'image/gif',
    		'png' => 'image/png',
            'swf' => 'application/x-shockwave-flash');


        // get banner row
        $sql = 'SELECT * FROM '.$table_banners.' WHERE '.$WHERE.' '.$ORDERBY.' LIMIT 1;';
        $Recordset = $wpdb->get_results( $sql  );
		$return = "";
        if ( count($Recordset) == 1 ) {
            $ext = wp_check_filetype ($Recordset[0]->file_path, $mimetypes );

            if ( $ext["ext"] == "swf" )  {
               $return = '<div id="wp-banner_div_'.$Recordset[0]->ID.'"></div>';
               $return = "<script type=\"text/javascript\">/* <![CDATA[ */
                 var so = new SWFObject(\"".get_bloginfo('url')."/wp-content/plugins/wp-addpub/swfload.swf?src=".urlencode($Recordset[0]->file_url)."&href=".urlencode($Recordset[0]->file_link)."&target=".$Recordset[0]->target."\", \"".addslashes($Recordset[0]->title)."\", \"".$Recordset[0]->width."\", \"".$Recordset[0]->height."\", \"".$Recordset[0]->version."\");
                 so.write(\"wp-banner_div_".$Recordset[0]->ID."\");
                 /* ]]> */
              </script>";
			  if ( $echo) {
				echo $return;
				return true;
			  } else {
			    return $return;
			  }			  
            } else{
              $return = stripslashes($Recordset[0]->html_code);
			  if ( $echo) {
				echo $return;			 
                return true;
			  } else {
				return $return;
			  }
            }
         } else {
            return false;
         }

}

